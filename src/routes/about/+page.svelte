<section class="max-w-4xl mx-auto px-4 py-12 mt-10">
    <h2 class="text-3xl font-bold mb-6 text-center">🔧 O projektu</h2>
  
    <p class="text-gray-700 mb-6">
      Tato aplikace je vlastní alternativou ke klasickému Tinderu – postavená s důrazem na jednoduchost, přehlednost a moderní technologie.
    </p>
  
    <h3 class="text-2xl font-semibold mt-8 mb-4">🧠 Technologie v pozadí:</h3>
    <ul class="list-disc list-inside space-y-2 text-gray-800">
      <li><strong>SvelteKit:</strong> moderní framework pro rychlý vývoj webových aplikací s vynikajícím výkonem</li>
      <li><strong>TypeScript:</strong> pro typovou bezpečnost a lepší čitelnost kódu</li>
      <li><strong>Prisma ORM:</strong> typově bezpečný přístup k databázi (např. SQLite nebo PostgreSQL)</li>
      <li><strong>SQLite:</strong> jednoduchá a rychlá databáze ideální pro vývoj a menší projekty</li>
      <li><strong>Tailwind CSS:</strong> utility-first stylování pro čistý design</li>
      <li><strong>Session-based autentizace:</strong> bezpečné přihlašování a správa uživatelských relací</li>
      <li><strong>bcrypt:</strong> hashování hesel pro bezpečné ukládání</li>
    </ul>
  
    <h3 class="text-2xl font-semibold mt-8 mb-4">⚙️ Funkce:</h3>
    <ul class="list-disc list-inside space-y-2 text-gray-800">
      <li>Registrace a přihlašování uživatelů</li>
      <li>Swipeování profilů (match systém)</li>
      <li>Ukládání vzájemných matchů</li>
      <li>Real-time chat mezi spárovanými uživateli</li>
      <li>Admin rozhraní pro správu uživatelů (volitelné)</li>
    </ul>
  
    <h3 class="text-2xl font-semibold mt-8 mb-4">🎯 Cíl projektu:</h3>
    <p class="text-gray-700">
      Vyzkoušet si vývoj moderní webové aplikace od backendu po frontend s využitím nejnovějších technologií a zároveň vytvořit funkční seznamovací platformu s potenciálem dalšího rozšiřování.
    </p>
